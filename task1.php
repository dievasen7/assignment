// 1. Извеждаме числа от 1 до 10, разделени с тире, 
// като преди 1 и след 10 не трябва да има тире:

for ($i = 1; $i <= 10; $i++){
    // Всички числа по-малки от 10 ги извеждаме с тире:
    if ($i < 10) {
        echo $i . '-';
    } else {
        // Kогато числото стане 10, го извеждаме без тире:
        echo $i;
    }
}